<p><strong>Aseidad</strong> nace en 1996 siendo uno de los primeros exponentes del Death Doom Metal en Chile la primera formación fue compuesta por : Marcelo Vivallo en voz, Mauricio Vivallo en bajo, Patricio Saavedra en guitarra y Luis Moya en batería.</p>
        
        <p align="center"><img src="images/aseidad1_bio.jpg" width="320" height="211" alt="aseidad 1999" class="pic_bio"/></p>
        
        <p>        La banda graba su primer demo en el año 1997 en los estudios Radiográfica (ex Attic), bajo la supervisión de Marcelo Morales y con el apoyo de Jano Lizana de <strong>Dominus Xul</strong> en primera guitarra. Cuatro temas fueron grabados para esta producción &quot;The Burn&quot;, &quot;Eternal Autumn&quot;, &quot;Angel Efluvium&quot; y &quot;The Father's Pride&quot;.</p>
        <p>        En el segundo semestre de 1998, se incorpora un nuevo guitarrista: Marcelo Prades.</p>
        <p>        En septiembre de 1999 se graba el LP Autumn en N-Música A.C., (ex estudios Bellavista); Luego del lanzamiento del disco en mayo del 2000 Aseidad tocó en varias partes de Santiago y regiones. </p>
        <p align="center"><img src="images/clasica.jpg" width="300" height="230" alt="aseidad 2000" class="pic_bio"/></p>
        <p>        En el 2001 Patricio Saavedra y Marcelo Prades dejan la banda y forman <strong>Underneath</strong>, por su parte Luis Moya pasa a formar parte de <strong>Poema Arcanus</strong> y Mauricio Vivallo se concentra en su banda <strong>Sarkus</strong>.</p>
        <p>        Luego de unos meses de receso <strong>Aseidad</strong> comienza la búsqueda de nuevos integrantes ingresando a la banda: Eric Birkner ex bajista de <strong>Astral Soul</strong>, Mario Chacana en Batería, Juan Pablo &quot;Palalo&quot; Binimelis en guitarra, Juan &quot;Subtenebras&quot; Escobar en teclados y Marcelo Vivallo se mantiene en las voces. Con esta formación la banda comenzó a trabajar en nuevo material principalmente de la mano Eric. Durante ese período la banda empezó a tocar mucho en vivo tanto en Santiago como en regiones con gran aceptación.</p>
        <p align="center"><img src="images/aseidad_2003.jpg" width="250" height="150" alt="aseidad 2003" class="pic_bio"/></p>
        <p>        En los primeros meses de 2004, Rodrigo “Skanner” Ramírez de la banda <strong>Vigilante</strong> reemplaza a Juan Escobar en los teclados.<br>
        Durante el mismo año la banda graba un promocional que contiene los temas ‘I Fall’ y ‘The Clust’.</p>
        <p>        En el año 2005 la banda la banda se disuelve por diferencias musicales entre sus integrantes.</p>
        <p>        A finales del 2014 Marcelo Vivallo y Eric Birkner reforman la banda a la cual se incorporan Pedro Fontecilla de <strong>Decem Maleficivm</strong> en guitarra, Johanna Sánchez ex <strong>Lex Natura</strong> en teclados y Carlos “Control” Cepeda ex <strong>Denun</strong> en batería con esta formación la banda vuelve a las pistas y comienza a tocar en vivo nuevamente.</p>
        <p align="center"><img src="images/aseidad_band_doom.jpg" alt="aseidad 2015" class="pic_bio"/></p>
        
        <p>      El año 2015 <strong>Aseidad</strong> graba su segundo LP ‘Voice of Snake’ en los Estudios Maestro3 de la mano de Claudio Salinas la placa tendrá su lanzamiento durante el primer trimestre del 2017.</p>
